        <h1 class="title">Talk! Call the Ball!</h1>
        <div class="content">
          <p>When a free-ball drops between players, the problem is often poor understanding of the team’s system —not a lack of talk.</p>
          <p>Short yells can confirm action or announce deviation from the norm, but talk is just a facilitator, not the foundation of great play. Team coordination emerges from players who —observing and thinking independently— share a mental model of what to do, how to do it, and who does what.</p>
          <p>Beginners have simple mental models; they’re more like rules or impulses than guidelines or reactions:</p>
          <ol>
            <li>When every kid on a soccer field runs to the ball —abandoning goalposts, defense, and dandelions— they think: <i>Get the ball.</i></li>
            <li>When two friends on a basketball team only pass to each other, they think: <i>My friend is good and passes back.</i></li>
            <li>When a team watches their front-row setter sprint to dive for a pass shanked to deep zone five, they think: <i>The setter always gets the second ball.</i></li>
          </ol>
          <p>It’s a coach's job to develop their players’ mental models through experience and feedback. They should run more games than drills, practice tough situations, and teach the reasoning behind their system. Player’s who understand why to react a certain way excel out-of-system and play creatively (see <a href="https://youtu.be/yF4j4EHgt30?t=198" target="_blank">beach players go over on one</a> and the <a href="https://youtu.be/hAQqJDCRB80?t=147" target="_blank">Brazilian national team sets free balls directly to their outsides</a>).</p>

          <p>I try to develop understanding by pausing games and asking questions: "If this happens, what do you think we should do? Why do you think that is?"</p>

          <p>A team that understands the motivation behind guidelines like the setter always gets the second ball —deliver the best possible scoring opportunity— would see situation three above and ask, “Could I better set this ball?"</p>

          <p>Talk is important, but team coordination emerges from a shared mental model of great play.</p>
